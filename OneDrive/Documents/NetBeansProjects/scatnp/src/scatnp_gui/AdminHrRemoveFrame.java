    private void btnRemoveHrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveHrActionPerformed
        // TODO add your handling code here:
             try
             {
                
                
                 if(tblAllHr.getRowCount()==0)
                 {
                     //button can be remove by set enabled method
                     JOptionPane.showMessageDialog(null,"No Hr Present!","No Hr",JOptionPane.INFORMATION_MESSAGE);
                     btnRemoveHr.setEnabled(false);
                   return;
                 }
                 int row=tblAllHr.getSelectedRow();
                String hrId= tblAllHr.getValueAt(row,0).toString();
               boolean res=UserDao.removeHr(hrId);
              
               if(res==true)
               {
                   model.removeRow(row);
                   JOptionPane.showMessageDialog(null,"Hr Remove Succesfuly","Hr removed",JOptionPane.INFORMATION_MESSAGE);
                   return;
               }
               else
               {
                    JOptionPane.showMessageDialog(null,"Hr Not Removed ","Hr  Not removed",JOptionPane.ERROR_MESSAGE);
               }
                 
                 
                 
             }
              catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Db Error while removing Hr!!","Db Error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
             
        
    }//GEN-LAST:event_btnRemoveHrActionPerformed

