    private void loadData()
    {
        try
        {
            List<HrPojo>allHrList=HrDao.getAllActiveHr();
            for(HrPojo hr:allHrList)
            {
                Vector<String> row=new Vector();
                row.add(hr.getHrId());
                row.add(hr.getUserId());
                row.add(hr.getHrName());
                row.add(hr.getMobileNo());
                row.add(hr.getMobileNo());
                model.addRow(row);
             
            }
              
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Db Error!!","Db Error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
   


