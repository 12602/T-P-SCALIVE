    private void btnEditJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditJobActionPerformed
        // TODO add your handling code here:
       int row= tblAllJobs.getSelectedRow();
      if(tblAllJobs.getRowCount()==0)
      {
          JOptionPane.showMessageDialog(null,"No Job Added!","No Job",JOptionPane.ERROR_MESSAGE);
          btnEditJob.setEnabled(false);
          return;
      }
       JobPojo job=new JobPojo();
      job.setJobId( tblAllJobs.getValueAt(row,0).toString());
      job.setJobTitle(tblAllJobs.getValueAt(row,1).toString());
      job.setTags(tblAllJobs.getValueAt(row, 2).toString().trim());
      job.setStatus(0);
       JOptionPane.showMessageDialog(null,job.getStatus());
      
        HrEditJobFrame hr=new HrEditJobFrame(job);
        hr.setVisible(true);
        
        this.dispose();
       

    }//GEN-LAST:event_btnEditJobActionPerformed

