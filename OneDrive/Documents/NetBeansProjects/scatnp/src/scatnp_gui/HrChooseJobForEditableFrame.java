    private void loadData()
    {
        try
        {
              
         
           List<JobPojo> allJobs= JobDao.getAllEditableJobByCurrentHr(CurrentUser.getId());
           for(JobPojo job:allJobs)
           {
               Vector<String> row=new Vector();
               row.add(job.getJobId());
               row.add(job.getJobTitle());
               row.add(job.getTags());
               
               row.add("0");
               
               model.addRow(row);
           }
         
         
            
        }
        catch(SQLException  sq)
                {
                    JOptionPane.showMessageDialog(null,"Error while loading jobs","Error",JOptionPane.ERROR_MESSAGE);
                    return;
                }
    }
    
       

