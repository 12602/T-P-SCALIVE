    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
        // TODO add your handling code here:
                JobPojo job=new JobPojo();
                job.setJobId(lblJobId.getText());
                job.setTags(txtSkill1.getText().trim()+","+txtSkill2.getText().trim()+","+txtSkill3.getText().trim()+","+txtSkill4.getText().trim());
                
                job.setJobTitle(txtJobTitle.getText());
                job.setStatus(0);
                JOptionPane.showMessageDialog(null,job.getStatus());
               try
               {
                  boolean res= JobDao.editJobByJobId(job);
                  
                   
                  if(res==true)
                  {
                    
                      JOptionPane.showMessageDialog(null,"Job Updated Succesfully!!","Job Updated",JOptionPane.INFORMATION_MESSAGE);
                     
                      return;
                      
                  }
                  else
                  {
                       JOptionPane.showMessageDialog(null,"Job Not Updated Succesfully!!","Job  Not Updated",JOptionPane.ERROR_MESSAGE);
                       return;
                  }
               }
               catch(SQLException sq)
               {
                    JOptionPane.showMessageDialog(null,"Db Error while updating job!!","DB ERROR",JOptionPane.ERROR_MESSAGE);
                    sq.printStackTrace();
                    return;
               }
                
                

    }//GEN-LAST:event_btnSaveChangesActionPerformed

