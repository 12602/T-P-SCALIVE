       public void loadData(JobPojo job)
       {
           try
           {
               
           
           txtJobTitle.setText(job.getJobTitle());
           
           txtCompanyName.setText(HrDao.getCompanyNameByHrId(CurrentUser.getId()));
           
           // spilliting the skills into 4 skills on , seprated
                 String []allSkills=job.getTags().split(",");
                 txtSkill1.setText(allSkills[0]);
            txtSkill2.setText(allSkills[1]);
              txtSkill3.setText(allSkills[2]);
              txtSkill4.setText(allSkills[3]);
              lblJobId.setText(job.getJobId());
                 
                 
           }
           catch(SQLException sq)
           {
               JOptionPane.showMessageDialog(null,"Error while loading Job ","Error",JOptionPane.ERROR_MESSAGE);
               return;
               
           }
       }
       

